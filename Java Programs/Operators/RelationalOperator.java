package OperatorsProgram;

public class RelationalOperator {

	public static void main(String[] args) {

		
		int a=450;
		int b=340;
		
		System.out.println(a > b);    //greater than opr
		System.out.println(a < b);   // less than opr
		System.out.println(a == b);   //equal to equalto opr
		System.out.println(a >= b);     //less than equal to opr
		System.out.println(a <= b);      //greater than equal to  opr
		System.out.println(a !=b);        // not equal to opr

	}

}
