
public class OddNumber {
	
    //Print ODD number from 1 to 10 WHILE LOOP


	public static void main(String[] args) {

		
	      int number = 1;
	      while(number <= 10)
	      {
	          if(number%2 != 0)
	          {
	              System.out.println(number);
	          }
	          ++number;
	      }
	    
	}

}
