
public class VowelsWhileLoop {

	public static void main(String[] args)
	{

		
		
	      char vowels[]={'a','e','i','u','o','s','a','t'};
	      int size = vowels.length;
	      int index = 0;
	      while(index < size)
	      {
	          System.out.println(vowels[index]);
	          index++;
	      }
	    
	}

}
